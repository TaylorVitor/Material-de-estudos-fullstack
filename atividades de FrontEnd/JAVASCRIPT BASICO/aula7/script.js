/* 
Adição: +
Subtração: -
Multiplicação: *
Divisão:/
incremento: ++
Decremento: --
Atribuição: += -=
*/

// Adição
let a = 10
let b = 23
let c = a + b
console.log(c)


// subtração
let d = 31
let e = 12
let f = d - e
console.log(f)


// Multiplicação
let g = 25
let h = 4
let i = g * h
console.log(i)

// divisão
let j = 30
let k = 2
let l = j / k
console.log(l)

//incremento
let m = 10
m++
console.log(m)


//discremento
let n = 10
n--
console.log(n)


//Atribuição
let o = 45
o += 20
console.log(o)

let p = 45
p -= 20
console.log(p)